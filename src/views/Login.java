    private void btnLoginKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_btnLoginKeyPressed
        // TODO add your handling code here: Avança para tela Listagem
        if(evt.getKeyCode() == java.awt.event.KeyEvent.VK_ENTER) {
            String username = txtLogin.getText();
            String password = new String(txtPassword.getPassword());
            
             // Verifica se os campos estão preenchidos
        if (username.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(this, 
                "Por favor, preencha todos os campos.", "Erro", JOptionPane.ERROR_MESSAGE);
        } else {
            // Verifica se o usuário existe e se a senha está correta
            if (usuarios.containsKey(username) && usuarios.get(username).getPassword().equals(password)) {
            // Recupera o usuário e avança para a tela de Listagem
            Usuario usuario = usuarios.get(txtLogin.getText());
            JOptionPane.showMessageDialog(Login.this, 
                "Olá " + usuario.getUsuario() + ", sua permissão é de: Lista\n  " + usuario.getTipo() + ". Seja bem-vindo!");
                  Listagem telaListagem = new Listagem(usuario);  // Passe o usuário como parâmetro
                  if (usuario.getTipo().equalsIgnoreCase("usuario")) {
                        telaListagem.setVisibilidadeBotoes(true, false, false);
                    } else if (usuario.getTipo().equalsIgnoreCase("operador")) {
                        telaListagem.setVisibilidadeBotoes(true, true, false);
            } else if (usuario.getTipo().equalsIgnoreCase("admin")) {
                        telaListagem.setVisibilidadeBotoes(true, true, true);
        }

                  
                  telaListagem.setVisible(true);
            dispose();
            
            
            
            /*Listagem telaListagem = new Listagem(usuario);
            telaListagem.setVisible(true);
            dispose();*/
            }else {
                // Se o usuário ou a senha estiverem incorretos, exibe uma mensagem de erro
                JOptionPane.showMessageDialog(this, 
                    "Usuário ou senha inválidos", "Erro", JOptionPane.ERROR_MESSAGE);
                }
            }

        }
    }//GEN-LAST:event_btnLoginKeyPressed

