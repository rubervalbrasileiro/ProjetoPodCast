    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here: Botão de Login
        // Recupera o usuário e a senha dos campos de texto
    String username = txtLogin.getText().toLowerCase();
    String password = new String(txtPassword.getPassword());

    // Verifica se o usuário existe e se a senha está correta
    if (usuarios.containsKey(username) && usuarios.get(username).getPassword().equalsIgnoreCase(password)) {
        // Recupera o objeto Usuario correspondente ao username
       Usuario usuario = usuarios.get(username);
        // Verifica o tipo de usuário e ajusta as permissões da interface
        if (usuarios.containsKey(username) && usuarios.get(username).getPassword().equalsIgnoreCase(password)) {
          usuario  = usuarios.get(username);
          //String tipoUsuario = usuario.getTipo();
          
          switch(username) {
              case "admin" :
                  JOptionPane.showMessageDialog(Login.this, 
                "Olá " + usuario.getUsuario() + ", sua permissão é de: Lista, Excluir, Cadastrar\n  " + usuario.getTipo() + ". Seja bem-vindo!");
                 
                  // abrirTelaPrincipal(usuario);
            Listagem telaListagem = new Listagem(usuario);  // Passe o usuário como parâmetro
            
            
            telaListagem.setVisible(true);
            dispose();
            break;
            
              case "operador":
                   JOptionPane.showMessageDialog(Login.this, 
                "Olá " + usuario.getUsuario().equalsIgnoreCase("operador") + ", sua permissão é de: Lista, Cadastrar\n  " + usuario.getTipo() + ". Seja bem-vindo!");
                  
                   // abrirTelaPrincipal(usuario);
            Listagem telaListagem1 = new Listagem(usuario);  // Passe o usuário como parâmetro
            telaListagem1.setVisible(true);
            dispose();
            break;
            
             case "usuario":
                  JOptionPane.showMessageDialog(Login.this, 
                "Olá " + usuario.getUsuario() + ", sua permissão é de: Lista\n  " + usuario.getTipo() + ". Seja bem-vindo!");
                  Listagem telaListagem2 = new Listagem(usuario);  // Passe o usuário como parâmetro
            telaListagem2.setVisible(true);
            dispose();
            break;
           
            default:
                JOptionPane.showMessageDialog(this, 
                        "Tipo de usuário não reconhecido!", "Erro", JOptionPane.ERROR_MESSAGE);
                break;
            }
        } 
    } else {
        // Se o usuário ou senha estiverem incorretos, exibe uma mensagem de erro
        JOptionPane.showMessageDialog(this, 
            "Usuário ou senha inválidos", "Erro", JOptionPane.ERROR_MESSAGE);
        }
               
    }//GEN-LAST:event_btnLoginActionPerformed

